	public static function search($input)
	{
		foreach (self::ALL_COLORS as $colors) {
			if ($input === $colors['label'] || $input === $colors['value']) {
				return $colors;
			}
		}

		return array_pop(self::ALL_COLORS);
	}

	public static function convertToString(array $colors, string $type = 'rgb')
	{
		if (count($colors) === 3) {
			return sprintf('%s(%s)', $type, implode(', ', $colors));
		} else if (count($colors) === 4) {
			return sprintf('%sa(%s)', $type, implode(', ', $colors));
		} else {
			return implode(', ', $colors);
		}
	}

	public static function generateCssTextColorClass(string $prefix = 'text-')
	{
		$cssList = array();

		foreach (ColorTypes::ALL_COLORS as $colors) {
			$selector = $prefix . str_replace(' ', '-', strtolower($colors['label']));
			$selector = explode('/', $selector);
			$selector = array_shift($selector);
			$cssList[] = sprintf('.%s { color: %s; }', $selector, ColorTypes::convertToString($colors['value']));
		}
		
		return implode("\n", $cssList);
	}

	public static function generateCssBgColorClass(string $prefix = 'bg-')
